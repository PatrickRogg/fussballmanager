package fussballmanager.mvc.kader;

public class SummeSpielerWerte {

	private int alter;
	
	private double staerke;
	
	private double motivation;
	
	private double reinStaerke;
	
	private double zuwachs;
	
	private double erfahrung;
	
	private double gehalt;

	public int getAlter() {
		return alter;
	}

	public void setAlter(int alter) {
		this.alter = alter;
	}

	public double getStaerke() {
		return staerke;
	}

	public void setStaerke(double staerke) {
		this.staerke = staerke;
	}

	public double getMotivation() {
		return motivation;
	}

	public void setMotivation(double motivation) {
		this.motivation = motivation;
	}

	public double getReinStaerke() {
		return reinStaerke;
	}

	public void setReinStaerke(double reinStaerke) {
		this.reinStaerke = reinStaerke;
	}

	public double getZuwachs() {
		return zuwachs;
	}

	public void setZuwachs(double zuwachs) {
		this.zuwachs = zuwachs;
	}

	public double getErfahrung() {
		return erfahrung;
	}

	public void setErfahrung(double erfahrung) {
		this.erfahrung = erfahrung;
	}

	public double getGehalt() {
		return gehalt;
	}

	public void setGehalt(double gehalt) {
		this.gehalt = gehalt;
	}
	
	
}
